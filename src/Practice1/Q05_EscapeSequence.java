package Practice1;

public class Q05_EscapeSequence {
    public static void main(String[] args) {
        // konsola hello world yazdiri
        System.out.println("hello world");

        // pazartesi kelimesini her harf ayri satira gelecek sekilde yazdir
        System.out.println("P\na\nz\na\nr\nt\ne\ns\ni");

        // java ile hayat cok 'afilli' yazdiriniz
        System.out.println("\"java\" ile hayat cok \'afilli\'");

        // "zaruret" insani
        // 'kasif'yapar
        System.out.println("\" zaruret\" insani \n \'kasif\' yapar \n \"fatih\" ile java cok kolay");

        // "FATIH Ile java cok guzel... " ifadesini 3 satir sonra ve satir basina yazdir, ustediki ile ayni



    }
}
